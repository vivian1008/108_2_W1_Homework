public class Book{
    private String name, writer, isbn, page, date;

    public void setName(String setname){
        this.name = setname;
    }

    public String getName(){
        return name;
    }

    public void setWriter(String setwriter){
        this.writer = setwriter;
    }

    public String getWriter(){
        return writer;
    }

    public void setIsbn(String setisbn){
        this.isbn = setisbn;
    }

    public String getIsbn(){
        return isbn;
    }

    public void setPage(String setpage){
        this.page = setpage;
    }

    public String getpage(){
        return page;
    }

    public void setDate(String setdate){
        this.date = setdate;
    }

    public String getDate(){
        return date;
    }

}